export default function FeatureProductSlide() {
  const [currentIndex, setCurrentIndex] = useState(0);

  const handlePrev = () => {
    setCurrentIndex((prevIndex) => (prevIndex - 1 + images.length) % images.length);
  };

  const handleNext = () => {
    setCurrentIndex((prevIndex) => (prevIndex + 1) % images.length);
  };

  return (<>
    <div className="flex justify-between  px-20">
      <label className="font-rubikmonoone">Featured</label>
      <label>All</label>
    </div>
    <div className="relative px-20">
      <div className="overflow-hidden">
        <div className="flex transition-transform duration-500 ease-in-out" style={{ transform: `translateX(-${currentIndex * (100 / 8)}%)` }}>
          {images.map((src, index) => (
            <div key={index} className="min-w-64 px-2">
              <img
                src={src}
                alt={`image ${index + 1}`}
                className="h-full w-full object-cover"
              />
              {/* <div className="text-center mt-2">Category {index + 1}</div> */}
            </div>
          ))}
        </div>
      </div>
      <button onClick={handlePrev} className="absolute h-full w-1/12 left-20 top-1/2 transform -translate-y-1/2 bg-gradient-to-r from-white/80">
        <FontAwesomeIcon
          className="text-orange-500 p-2 -translate-y-1/2 -left-3 absolute rounded-full bg-white border-orange-500 border"
          icon={faArrowLeft} />
      </button>
      <button onClick={handleNext} className="absolute h-full w-1/12 right-20 top-1/2 transform -translate-y-1/2 bg-gradient-to-l from-white/80">
        <FontAwesomeIcon
          className="text-orange-500 p-2 -translate-y-1/2 -right-3 absolute rounded-full bg-white border-orange-500 border"
          icon={faArrowRight} />
      </button>
    </div>
  </>
  );
}
